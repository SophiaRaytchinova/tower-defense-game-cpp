//част от директния достъп до елементите в масивът от символи (map[ROWS][COLS])
const int ROWS=10;
const int COLS=10;

//функция за изчисляване на новите координати на врага
void update(const char* map[ROWS][COLS], int &enemyX, int &enemyY){
    /*
    хардкоднат път на врага чрез вектор от двойки координати
    в двумерен масив от символи сме->двойки
    минава по такъв път, че да стигне до базата
    */
    std::vector<std::pair<int, int>> path = {
        {1,1}, {1,2}, {2,2}, {3,2}, {3,3}, {3,4}, {3,5}, {3,6},
        {4,6}, {5,6}, {6,6}, {7,6}, {8,6}, {8,7}, {8,8}, {8,9}
    };

    //ако врагът не е в края на пътя, премести го на следващата позиция
    for (int i=0; i<path.size(); i++){
        if (path[i].first == enemyX && path[i].second == enemyY){
            if (i+1 < path.size()){
                //изчистване на старата позиция
                map[enemyX][enemyY] = ".";
                //актуализиране на координатите на врага
                enemyX = path[i+1].first;
                enemyY = path[i+1].second;
                //поставяне на врага на новата позиция
                map[enemyX][enemyY] = "E";
            }
        }
    }
}

//функция за изчистване на конзолния екран и извеждане на света наново
void render (const char* map[ROWS][COLS]){
    //изчистване на конзолния екран
    system("cls");
    //извеждане на света в конзолата наново
    for (int i=0; i<ROWS; i++){
        for (int j=0; j<COLS; j++){
           cout<<map[i][j];
        }
        cout<<endl;
    }
}

//функция за забавяне на изпълнението на програмата, 
//за да не се телепортира врагът
void timing(){
    std::this_thread::sleep_for(std::chrono::milliseconds(1000));
}

int main(){

    //представяне на света като двумерен масив от символи
    const char* map[ROWS][COLS]={
        {".", ".", ".", "#", "#", "#", "#", "#", "#", "#"},
        {".", "E", ".", ".", ".", ".", ".", ".", ".", "#"},
        {".", ".", ".", "#", "#", "#", "#", "#", ".", "#"},
        {".", ".", ".", ".", ".", ".", ".", "#", ".", "#"},
        {"#", "#", "#", "#", "#", "#", ".", "#", ".", "#"},
        {".", ".", ".", ".", ".", "#", ".", "#", ".", "#"},
        {".", "#", "#", "#", ".", "#", ".", "#", ".", "#"},
        {".", ".", ".", "#", ".", "#", ".", "#", ".", "#"},
        {".", "#", ".", "#", ".", "#", ".", ".", ".", "B"},
        {".", "#", "#", "#", "#", "#", "#", "#", "#", "#"}
    };

    //дефиниране на символи за празна земя, път/стена, враг и база
    const char emptyGround='.';
    const char pathWall='#';
    const char enemy='E';
    const char base='B';

    //стартови координати на врага, базирани на зададената карта
    int enemyX=1;
    int enemyY=1;

    //Update-Render
    while(true){ //безкраен while цикъл
        update(map, enemyX, enemyY); //ъпдейт на позицията на врага
        render(map); //изчистване на конзолата и рисуване на света наново
        timing(); //забавяне на изпълнението на програмата да не се телепортира врагът
    }

    return 0;
}


